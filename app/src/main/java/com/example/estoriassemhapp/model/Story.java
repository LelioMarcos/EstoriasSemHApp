package com.example.estoriassemhapp.model;

public class Story {
    String id;
    String title;
    String text;

    public Story(String id, String title, String text) {
        this.id = id;
        this.title = title;
        this.text = text;
    }

    public String getId() {
        return id;
    }

    public String getTitle() {
        return title;
    }

    public String getText() {
        return text;
    }
}
